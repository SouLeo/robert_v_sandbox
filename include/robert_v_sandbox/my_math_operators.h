/**
 * @brief math_operators namespace
 * 
 */
namespace math_operators
{
  /**
   * @brief Adds two numbers
   * 
   * @param num_a first input number
   * @param num_b second input number
   * @return double result of adding the two input numbers
   */
  double add(double num_a, double num_b);

  /**
   * @brief Subtracts two numbers
   * 
   * @param num_a first input number
   * @param num_b second input number
   * @return double result of subtracting the two input numbers
   */
  double subtract(double num_a, double num_b);
}